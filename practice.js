// Write a function that returns the number of vowels
// used in a string.  Vowels are the characters 'a', 'e'
// 'i', 'o', and 'u'.
// --- Examples
//   vowels('Hi There!') --> 3
//   vowels('Why do you ask?') --> 4
//   vowels('Why?') --> 0

//iterative solution
function vowels(str){
    counter = 0
    
    for (char in str){
        if(char.includes('aeiou')){
            counter += 1 || 1
        }
    }
    return counter

}

